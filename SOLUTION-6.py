#WAS to enter any number from 1 to 7 and print appropiate day name.
n=int(input("Enter Number:"))
if(n==1):
	print("It is Monday")
elif(n==2):
	print("It is Tuesday")
elif(n==3):
	print("It is Wednesday")
elif(n==4):
	print("It is Thursday")
elif(n==5):
	print("It is Friday")
elif(n==6):
	print("It is Saturday")
elif(n==7):
	print("It is Sunday")
else:
    print(" Sorry Please Enter Appropriate Number!")
